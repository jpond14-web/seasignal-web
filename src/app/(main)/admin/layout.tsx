"use client";

import Link from "next/link";
import { usePathname, useRouter } from "next/navigation";
import { useEffect, useState } from "react";
import { createClient } from "@/lib/supabase/client";

const adminNavItems = [
  { href: "/admin", label: "Dashboard", icon: DashIcon, exact: true },
  { href: "/admin/users", label: "Users", icon: UsersIcon, exact: false },
  { href: "/admin/companies", label: "Companies", icon: CompanyIcon, exact: false },
  { href: "/admin/vessels", label: "Vessels", icon: VesselIcon, exact: false },
  { href: "/admin/forums", label: "Forums", icon: ForumIcon, exact: false },
  { href: "/admin/reviews", label: "Reviews", icon: ReviewIcon, exact: false },
  { href: "/admin/incidents", label: "Incidents", icon: IncidentIcon, exact: false },
  { href: "/admin/verify", label: "Verification", icon: VerifyIcon, exact: false },
  { href: "/admin/reports", label: "Reports", icon: ReportIcon, exact: false },
  { href: "/admin/flares", label: "Signal Flares", icon: FlareIcon, exact: false },
  { href: "/admin/advocacy", label: "Advocacy", icon: AdvocacyIcon, exact: false },
  { href: "/admin/channels", label: "Channels", icon: ChannelIcon, exact: false },
  { href: "/admin/audit", label: "Audit Log", icon: AuditIcon, exact: false },
];

export default function AdminLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const pathname = usePathname();
  const router = useRouter();
  const [authorized, setAuthorized] = useState<boolean | null>(null);

  useEffect(() => {
    async function checkAdmin() {
      const supabase = createClient();
      const {
        data: { user },
      } = await supabase.auth.getUser();
      if (!user) {
        router.push("/login");
        return;
      }
      const { data: profile } = await supabase
        .from("profiles")
        .select("is_admin")
        .eq("auth_user_id", user.id)
        .single();
      setAuthorized(!!profile?.is_admin);
    }
    checkAdmin();
  }, [router]);

  if (authorized === null) {
    return (
      <div className="flex items-center justify-center py-20">
        <p className="text-slate-400">Checking access...</p>
      </div>
    );
  }

  if (!authorized) {
    return (
      <div className="flex items-center justify-center py-20">
        <div className="bg-navy-900 border border-red-500/30 rounded-lg p-8 text-center max-w-md">
          <div className="w-12 h-12 rounded-full bg-red-500/10 flex items-center justify-center mx-auto mb-4">
            <svg className="w-6 h-6 text-red-400" viewBox="0 0 20 20" fill="currentColor">
              <path fillRule="evenodd" d="M5 9V7a5 5 0 0110 0v2a2 2 0 012 2v5a2 2 0 01-2 2H5a2 2 0 01-2-2v-5a2 2 0 012-2zm8-2v2H7V7a3 3 0 016 0z" clipRule="evenodd" />
            </svg>
          </div>
          <h1 className="text-xl font-bold text-slate-100 mb-2">Access Denied</h1>
          <p className="text-slate-400 text-sm">
            You need to be a verified admin to access this area.
          </p>
          <Link
            href="/home"
            className="inline-block mt-4 px-4 py-2 bg-navy-800 border border-navy-600 rounded text-sm text-slate-300 hover:bg-navy-700 transition-colors"
          >
            Back to Dashboard
          </Link>
        </div>
      </div>
    );
  }

  return (
    <div className="flex flex-col lg:flex-row gap-6 max-w-7xl mx-auto w-full">
      <aside className="lg:w-56 shrink-0">
        <nav className="flex lg:flex-col gap-1 overflow-x-auto lg:overflow-x-visible pb-2 lg:pb-0 lg:sticky lg:top-6">
          <div className="hidden lg:block mb-3">
            <h2 className="text-xs font-semibold uppercase tracking-wider text-slate-500 px-3">
              Admin Panel
            </h2>
          </div>
          {adminNavItems.map((item) => {
            const isActive = item.exact
              ? pathname === item.href
              : pathname.startsWith(item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-2.5 px-3 py-2 text-sm rounded-lg whitespace-nowrap transition-colors ${
                  isActive
                    ? "text-teal-400 bg-navy-800 border border-navy-600"
                    : "text-slate-400 hover:text-slate-100 hover:bg-navy-800/50 border border-transparent"
                }`}
              >
                <item.icon className="w-4 h-4 shrink-0" />
                <span>{item.label}</span>
              </Link>
            );
          })}
        </nav>
      </aside>
      <div className="flex-1 min-w-0">{children}</div>
    </div>
  );
}

